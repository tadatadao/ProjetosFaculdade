/*
2) Faça um algoritmo que exiba todos números de 100 a 1.
*/

#include <stdio.h>
#include <stdlib.h>

int main(){

    int numero;

    printf("Este programa contara de 100 ate 1.\n\n");

    for(numero = 100; numero >= 1; numero--){

        printf("%i\n", numero);

    }//for decremento de 100 a 1

return 0;
}//main
