/*
Faça um algoritmo que exiba todos números de 100 a 1.
*/

#include <stdio.h>
#include <stdlib.h>

int main() {

    int contador = 100;

    while (contador >= 1) {
        printf("%i\n", contador);
        contador--;
    }//while

    printf("\nEstes sao os numeros inteiros de 100 a 1.\n");

return 0;
}//main
