/*
1) Faça um algoritmo que exiba todos números de 1 a 100.
*/

#include <stdio.h>
#include <stdlib.h>

int main() {

    int contador = 1;

    while (contador <= 100) {
        printf("%i\n", contador);
        contador++;
    }//while

    printf("\nEstes sao os numeros inteiros de 1 a 100.\n");

return 0;
}//main
